#include <iostream>
using namespace std;
int main() {


        int i,j;
        i=5;
        cout << "nilai i awal : " << i << endl;
        cout << "nilai ++1    : " << ++i << endl;
        cout << "nilai i ahir : " << i <<endl;

        j=10;
        cout << "nilai j awal : " << j << endl;
        cout << "nilai ++j    : " << j++ << endl;
        cout << "nilai j ahir : " << j <<endl;
        return 0;
}
