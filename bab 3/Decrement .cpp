#include <iostream>
using namespace std;

int main()
{
    int y;
    float l;
    y=100;
    l=10.5;

    cout << "nilai y awal  : " << y << endl;
    cout << "nilai --y     : " << --y<< endl;
    cout << "nilai y ahir  : " << y << endl;

    cout << "nilai l awal  : " << l << endl;
    cout << "nilai --     : " << l-- << endl;
    cout << "nilai l ahir  : " << l  << endl;

    return 0;
}
